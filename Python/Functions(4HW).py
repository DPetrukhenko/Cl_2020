def password_generator(num, upper_case, characters):
    """функция password_generator создает пароль по 3м заданным аргументам: 1-ый - количество цифр,
       2-й - количество заглавных букв, 3-й - общее количество символов. Возвращает случайно сгенерированный пароль
       по заданным параметрам"""
    passw = []
    # добавляем случайные цифры
    for elem in range(num):
        passw.append(random.choice('123456789'))
    # добавляем заглавные буквы
    for elem in range(upper_case):
        passw.append(random.choice('QWERTYUIOPASDFGHJKLZXCVBNM'))
    # добавляем строчные буквы
    for elem in range(characters - (num + upper_case)):
        passw.append(random.choice('qwertyuiopasdfghjklzxcvbnm'))
    # перемешиваем список
    random.shuffle(passw)
    # соединяем список в строку
    password = ''.join(passw)
    return password


print(password_generator(4, 3, 15))
