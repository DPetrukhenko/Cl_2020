def translit(text):
    """Функция translit осуществляет преобразование текста на кириллице в текст на латинице.
    У функции один аргумент - переменная с текстом(на кириллице), в качестве значения возвращает текст на латинице."""
    # создаем словарь с побуквенными соотвествиями
    transliteration = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo', 'ж': 'zh',
        'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o',
        'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts',
        'ч': 'ch', 'ш': 'sh', 'щ': 'shch', 'ы': 'y', 'ю': 'yu', 'я': 'ya', 'А': 'A', 'Б': 'B',
        'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
        'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S',
        'Т': 'T', 'У': 'U', 'Ф': 'F', 'Х': 'Kh', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh',
        'Щ': 'Shch', 'Ы': 'Y', 'Ю': 'Yu', 'Я': 'Ya', 'ь': '', 'ъ': '', 'Ь': '', 'Ъ': ''
    }
    # йотированное е
    exceptions = ['ъе', 'ье', 'уе', 'ае', 'ое', 'ие', 'ее', 'ые', 'эе', 'яе', 'юе', 'ёе', ' е']
    # создаем пустую переменную для обработанного текста
    transliterated_text = ''
    # заменяем йотированные е
    for elem in exceptions:
        if elem in text:
            text = text.replace(elem, elem[0] + 'йе')
    # сам цикл транслитерации
    for elem in text:
        if elem in transliteration:
            transliterated_text += transliteration[elem]
        else:
            transliterated_text += elem
    return transliterated_text


